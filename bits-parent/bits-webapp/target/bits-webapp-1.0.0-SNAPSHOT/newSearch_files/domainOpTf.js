$(document).ready(function () {
    $("#searchdomainone>div>span").click(function () {
        var clickDiv = $(this).parent();
        if (clickDiv.next().is(":hidden")) {
            clickDiv.addClass("on");
            clickDiv.next().show();
        }
        else {
            clickDiv.removeClass("on");
            clickDiv.next().hide();
        }
    });
    $("#searchdomaintwo>div>span").click(function () {
        var clickDiv = $(this).parent();
        if (clickDiv.next().is(":hidden")) {
            clickDiv.addClass("on");
            clickDiv.next().show();
        }
        else {
            clickDiv.removeClass("on");
            clickDiv.next().hide();
        }
    });
});